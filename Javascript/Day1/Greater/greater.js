//Greater than

var num1 = parseFloat(prompt("Enter number 1"));
var num2 = parseFloat(prompt("Enter number 2"));

if (num1 == num2) {
  document.write("Number 1 is Equal to Number 2");
} else if (num1 > num2) {
  document.write("Number 1 is greater");
} else {
  document.write("Number 2 is Greater");
}
